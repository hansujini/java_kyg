package day4;

public class ForreverseNumEx1 {

	public static void main(String[] args) {
		/* 5부터 1까지 출력하는 예제를 for문을 이용하여 작성하세요. */
		int i = 0;
		for(i = 5 ; i >= 1; i--) {
			System.out.print(i + " ");
		}
	System.out.println();
	for (i = 0 ; i < 5 ; i++ ) {
		System.out.print(5 - 1 + " ");
	}
	}
}
